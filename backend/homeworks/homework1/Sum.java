public class Sum
{
    public static void main(String[] args)
    {
        int sum = 0;
        for(int i=1; i<=10; i++)
        {
            sum += i;
        }
        System.out.println("Sum: " + sum);

        int a=1;
        while(a<=10){
            System.out.println(a);
            a++;
        }
    }


}
